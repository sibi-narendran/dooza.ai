// Helper function to get FAQ schema for a blog post
export const generateFAQSchema = (faqItems) => ({
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": faqItems.map(item => ({
        "@type": "Question",
        "name": item.question,
        "acceptedAnswer": {
            "@type": "Answer",
            "text": item.answer
        }
    }))
});

// Helper function to get breadcrumb schema
export const generateBreadcrumbSchema = (items) => ({
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    "itemListElement": items.map((item, index) => ({
        "@type": "ListItem",
        "position": index + 1,
        "name": item.name,
        ...(item.url && { "item": item.url })
    }))
});

// Helper function to generate complete Article schema
export const generateArticleSchema = (post, siteUrl = 'https://www.dooza.ai') => ({
    "@context": "https://schema.org",
    "@type": "Article",
    "headline": post.title,
    "description": post.excerpt,
    "image": `${siteUrl}${post.image}`,
    "author": {
        "@type": "Organization",
        "name": post.author,
        "url": siteUrl
    },
    "publisher": {
        "@type": "Organization",
        "name": "Dooza",
        "logo": {
            "@type": "ImageObject",
            "url": `${siteUrl}/logo.png`
        }
    },
    "datePublished": post.date,
    "dateModified": post.modifiedDate || post.date,
    "mainEntityOfPage": {
        "@type": "WebPage",
        "@id": `${siteUrl}/blog/${post.slug}`
    },
    "timeRequired": `PT${post.readTimeMinutes}M`,
    "articleSection": post.category,
    "keywords": post.tags.join(', ')
});
